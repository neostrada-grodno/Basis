    private void showTable() {
        Vector<String> tableHeaders = new Vector<String>();
        Vector tableData = new Vector();
        tableHeaders.add("Товар");
        tableHeaders.add("Приход");
        tableHeaders.add("Количество");

        Session session = HUtil.getSession();
        Nomenclature n = (Nomenclature) HUtil.getElement("Nomenclature", productCode, session);

        ArrayList res = HUtil.getShipments(productCode, Calendar.getInstance().getTime());
        for (int i = 0; i < res.size(); i++) {
            HashMap iRow = (HashMap) res.get(i);
            Vector oneRow = new Vector();
            oneRow.add(n.getName());
            Incoming in = (Incoming) HUtil.getElement("Incoming", (Integer) iRow.get("incomingCode"), session);
            oneRow.add(in.getNumber());
            oneRow.add(iRow.get("balance"));
            tableData.add(oneRow);
        }

        session.close();
        
        jtBalance.setModel(new DefaultTableModel(tableData, tableHeaders));
        Util.autoResizeColWidth(jtBalance);

    }
    
