    @Action
    public void jbOK() {
        if (jtShipments.getSelectedRow() != -1) {
            parent.setIncomingItem(
                    new Integer(jtShipments.getModel().getValueAt(jtShipments.getSelectedRow(), 0).toString()), row);
            parent.fixQuantity(
                    productCode,
                    new Integer(jtShipments.getModel().getValueAt(jtShipments.getSelectedRow(), 0).toString()),
                    row);
        }
        Util.closeJIF(this, parent, salesView);
        Util.closeJIFTab(this, salesView);
    }
