    public ShipmentsView(IOutcoming parent, SalesView salesView, int productCode, int row) {
        initComponents();

        Util.initJIF(this, "Партии", parent, salesView);
        Util.initJTable(jtShipments);

        this.parent = parent;
        this.salesView = salesView;
        this.productCode = productCode;
        this.row = row;

        setLocation((salesView.getFrame().getWidth() - getWidth()) / 2,
                (salesView.getFrame().getHeight() - getHeight()) / 2);

        Vector<String> tableHeaders = new Vector<String>();
        Vector tableData = new Vector();
        tableHeaders.add("Код");
        tableHeaders.add("Номер");
        tableHeaders.add("Дата");
        tableHeaders.add("Кол-во");
        tableHeaders.add("Цена");

        SimpleDateFormat sdfd = new SimpleDateFormat("dd.MM.yyyy");

        List ss = HUtil.getShipments(productCode, Calendar.getInstance().getTime());

        Session session = HUtil.getSession();

        for (int j = 0; j < ss.size(); j++) {
            HashMap se = (HashMap) ss.get(j);
            Vector<Object> oneRow = new Vector<Object>();
            Incoming i = (Incoming) HUtil.getElement("Incoming", (Integer) se.get("incomingCode"), session);
            if (i != null) {
                oneRow.add(i.getCode());
                oneRow.add(i.getNumber());
                oneRow.add(sdfd.format(i.getDatetime()));
                oneRow.add(se.get("balance"));
                oneRow.add(se.get("price"));
                tableData.add(oneRow);
            }
        }

        session.close();

        jtShipments.setModel(new MyTableModel(tableData, tableHeaders));
        Util.autoResizeColWidth(jtShipments);

    }

    private class MyTableModel extends DefaultTableModel {

        public MyTableModel(Vector table, Vector header) {
            super(table, header);
        }

        @Override
        public boolean isCellEditable(int r, int c) {
            return false;
        }
    }

