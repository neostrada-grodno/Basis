    private void save() {

        try {
            Session session = HUtil.getSession();
            Banks b = (Banks) HUtil.getElement("Banks", elementCode, session);
            session.beginTransaction();
            if (b == null) {
                b = new Banks();
                b.setName(jtfName.getText());
                b.setAddress(jtaAddress.getText());
                b.setBankCode(jtfBankCode.getText());
                b.setUnt(jtfUnt.getText());
                b.setLocked(0);
                session.save(b);
            } else {
                b.setName(jtfName.getText());
                b.setAddress(jtaAddress.getText());
                b.setBankCode(jtfBankCode.getText());
                b.setUnt(jtfUnt.getText());
                b.setLocked(0);
                session.merge(b);
            }
            session.getTransaction().commit();
            session.close();
            Util.updateJournals(salesView, BanksView.class);
        } catch (Exception e) {
            logger.error(e);
        }
    }

    public void close() {

        if (lock == 0) {
            if (hash == null || !hash.equals(Util.hash(this))) {
                int a = JOptionPane.showOptionDialog(
                        this, "Сохранить элемент?", "Сохранение", JOptionPane.YES_NO_CANCEL_OPTION,
                        JOptionPane.QUESTION_MESSAGE, null, new Object[]{"Да", "Нет"}, "Да");
                if (a == 0) {
                    save();
                }
            }
            Session session = HUtil.getSession();
            Banks b = (Banks) HUtil.getElement("Banks", elementCode, session);
            if (b != null && lock == 0) {
                session.beginTransaction();
                b.setLocked(0);
                session.merge(b);
                session.getTransaction().commit();
            }
            session.close();
        }
    }

    @Action
    public void closeEl() {
        close();
        Util.closeJIF(this, parent, salesView);
        Util.closeJIFTab(this, salesView);
    }
