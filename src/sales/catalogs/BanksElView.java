    public BanksElView(SalesView salesView, BanksView parent, Integer elementCode) {
        initComponents();

        Util.initJIF(this, "Банк", parent, salesView);

        this.salesView = salesView;
        this.parent = parent;
        this.elementCode = elementCode;

        Session session = HUtil.getSession();
        Banks b = null;
        if (elementCode != null) {
            b = (Banks) HUtil.getElement("Banks", elementCode, session);
        }

        if (b != null) {
            jlCode.setText("Код: " + b.getCode());
            jtfName.setText(b.getName());
            jtaAddress.setText(b.getAddress());
            jtfBankCode.setText(b.getBankCode());
            jtfUnt.setText(b.getUnt());
            lock = b.getLocked();
            if (lock == 1) {
                JOptionPane.showMessageDialog(
                        this,
                        "Элемент редактируется другим пользователем и будет открыт только для просмотра!",
                        "Блокировка", JOptionPane.PLAIN_MESSAGE);
                jtfBankCode.setEditable(false);
                jtfName.setEditable(false);
                jtaAddress.setEditable(false);
                jtfUnt.setEditable(false);
            } else {
                session.beginTransaction();
                b.setLocked(1);
                session.merge(b);
                session.getTransaction().commit();
            }
        } else {
            jlCode.setText("Код: " + HUtil.getNextMax("Banks", "code", session));
            lock = 0;
        }
        session.close();

        if(b != null) {
            hash = Util.hash(this);
        } else {
            hash = null;
        }
    }

